  init() {
    this.lang = localStorage.getItem(this.LANG_KEY) || 'de';
  },

  t(key, params) {
    const tr = this.translations[this.lang] || this.translations['de'];
    let text = tr[key] !== undefined ? tr[key] : (this.translations['de'][key] || key);
    if (params) {
      Object.keys(params).forEach(k => {
        text = text.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
      });
    }
    return text;
  },

  setLang(lang) {
    this.lang = lang;
    localStorage.setItem(this.LANG_KEY, lang);
    document.documentElement.lang = lang;
    this.applyStatic();
    this.updateLangButtons();
    if (document.body.classList.contains('eltern-modus')) {
      if (typeof ParentApp !== 'undefined') ParentApp.render();
    } else if (typeof App !== 'undefined' && App.currentPage) {
      App.navigateDirect(App.currentPage);
    }
  },

  applyStatic() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      el.textContent = this.t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = this.t(el.dataset.i18nPh);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      el.title = this.t(el.dataset.i18nTitle);
    });
  },

  updateLangButtons() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.toggle('active', btn.dataset.lang === this.lang);
    });
  },

  getLangSwitcherHTML() {
    const langs = [
      { code: 'de', flag: '🇩🇪', label: 'Deutsch' },
      { code: 'tr', flag: '🇹🇷', label: 'Türkçe' },
      { code: 'it', flag: '🇮🇹', label: 'Italiano' },
      { code: 'es', flag: '🇪🇸', label: 'Español' },
      { code: 'fr', flag: '🇫🇷', label: 'Français' },
      { code: 'en', flag: '🇬🇧', label: 'English' },
    ];
    return langs.map(l =>
      `<button class="lang-btn ${l.code === this.lang ? 'active' : ''}" data-lang="${l.code}"
        title="${l.label}" onclick="I18n.setLang('${l.code}')">${l.flag}<span class="lang-code">${l.code.toUpperCase()}</span></button>`
    ).join('');
  }
